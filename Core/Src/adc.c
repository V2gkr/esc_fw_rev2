#define TABLE_SIZE (sizeof(temp_table)/sizeof(temp_table[0]))
#define R_FIXED 15000.0f

float ntc_get_resistance(uint16_t adc)
{
    if (adc == 0) return 1e9; // защита (обрыв)

    return R_FIXED * ((REF / adc) - 1.0f);
}


float ntc_get_temperature(float R)
{
    // выход за пределы (сверху)
    if (R >= resistance[0])
        return temp_table[0];

    // выход за пределы (снизу)
    if (R <= resistance[TABLE_SIZE - 1])
        return temp_table[TABLE_SIZE - 1];

    // поиск диапазона
    for (int i = 0; i < TABLE_SIZE - 1; i++)
    {
        if (R <= resistance[i] && R >= resistance[i + 1])
        {
            float R1 = resistance[i];
            float R2 = resistance[i + 1];

            float T1 = temp_table[i];
            float T2 = temp_table[i + 1];

            // линейная интерполяция
            float T = T1 + (R - R1) * (T2 - T1) / (R2 - R1);

            return T;
        }
    }

    return -999.0f; // ошибка
}
float TempCelsius=0;
void NTC_Service(void)
{
    float R = ntc_get_resistance(HAL_ADC_GetValue(&hadc1));
    HAL_ADC_Start(&hadc1);
    TempCelsius=ntc_get_temperature(R);
}



uint16_t VBUS_measurement_samples;
float VBUS_measurement_volt;
float voltage_pre_div;

void VbusService(void){
  VBUS_measurement_samples=HAL_ADC_GetValue(&hadc2);
  HAL_ADC_Start(&hadc2);
  voltage_pre_div=(float)((VBUS_measurement_samples*REF)/ADC_RESOLUTION);
  VBUS_measurement_volt=(float)(voltage_pre_div/(float)((float)VBUS_R2/(float)(VBUS_R1+VBUS_R2)));
}
